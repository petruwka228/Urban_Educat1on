import numpy as np

# 1. Создание массива чисел
arr = np.array([10, 20, 30, 40, 50])
print("Исходный массив", arr)

# 2. Умножение всех элементов массива на 2
multiplied = arr * 2
print("Умножение на 2", multiplied)

# 3. Вычисление суммы всех элементов
sum_elements = np.sum(arr)
print("Сумма элементов", sum_elements)
